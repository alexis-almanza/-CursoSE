package cursose;

public class Switch {
	public static void main(String[] args) {
		int valor = 5;
		
		switch(valor) {
		case 1: 
			System.out.println("valor 1 "); 
			break;
		case 2:
			System.out.println("valor 2 "); 
			break;
		case 3:
			System.out.println("valor 3 "); 
			break;
			default:
				System.out.println("no se encontro valor "); 
				break;
		}
	}

}
